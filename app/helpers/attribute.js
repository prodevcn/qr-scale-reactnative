export function getAttributeLocaleText(selectedLocale, attribute) {
  if (!attribute) {
    return null;
  }
  const {labelList, code} = attribute;

  const localeName = labelList.find(
    (label) => label.localeId === selectedLocale.id,
  );

  if (!localeName) {
    return code;
  }
  return localeName.text || code;
}

