export function getFieldTypeByAttributeType(attributeType, usageType = null) {
  const attributeTypeToFieldType = {
    Text: 'text',
    TextArea: 'textarea',
    Number: 'number',
    Price: 'number',
    Date: 'date',
    Identifier: 'text',
    SimpleSelect: 'select',
    MultiSelect: 'select',
    Metric: 'select',
    Image: 'file',
    File: 'file',
    Video: 'file',
    YesNo: 'toggle',
    Link: 'text',
    AutoGenerated: 'text',
    CustomEntity: 'select',
  };

  const imageTypeToFieldType = {
    Link: 'text',
    Uploaded: 'file',
  };

  const videoTypeToFieldType = {
    Embedded: 'textarea',
    Link: 'text',
    Uploaded: 'file',
  };

  switch (attributeType) {
    case 'Image':
      return imageTypeToFieldType[usageType];
    case 'Video':
      return videoTypeToFieldType[usageType];
    default:
      return attributeTypeToFieldType[attributeType];
  }
}
